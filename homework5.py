immutable_var = 1, 2, "ttt"
print("Immutable var:", immutable_var)
# Кортеж – это неизменяемая упорядоченная коллекция, которая может содержать в себе разные типы данных.
# К неизменяемым объектам относятся числа и строки. В данном случае: 1, 2, "ttt"

mutable_list = [1, 2, "ttt"]
mutable_list[1] = 5
mutable_list.append("qqq")
print("Mutable list:", mutable_list)


